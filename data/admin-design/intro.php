<h2>About APENCMS (v0.9)</h2>
<p>APENCMS is the tiniest CMS you can find around. It is fully file-based and does not use a database, has extremely easy, simple, and friendly user interface.</p>
<p>APENCMS is a fork of NanoCMS (v0.4) by <a href="http://www.kalyanchakravarthy.net">Kalyan Chakravarthy</a>.
<p>Pages can be created with APENCMS without having to change any code, and links to the new pages are generated and inserted automatically.
Customizing the design/look and feel is very simple too, making it ideal for newbies.</p>

<p> You can visit the website (<a href="http://apencms.com">APENCMS.com</a>) for latest news and updates.</p>
